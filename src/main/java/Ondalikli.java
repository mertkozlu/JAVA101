public class Ondalikli {
    public static void main(String[] args) {
        // ondalıklı sayıları belirten veri tipleri ; float v double
        /* float da tam sayı yazdığında dahi onu ondalık sayı olarak verir.
           float kullanmak için vereceğimiz değerin sonuna 'F' veya 'f' ile yazarız.
         */
        float vFl = 3;
        float vFloa = 3.14f;
        float vFloat = 3.15F;
        System.out.println(vFl);
        System.out.println(vFloa);
        System.out.println(vFloat);
        double vDouble = 3.10;
        System.out.println(vDouble);
    }
}
